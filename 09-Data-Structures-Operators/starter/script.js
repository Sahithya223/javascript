//Arrays destructuring
const arr=[10,20,30];
const a=arr[0];
const b=arr[1];
const c=arr[2];

//other way of array destrucring declaring all the variables at a time.
const[x,y,z]=arr;
console.log(x,y,z);//output will be 10,20,30

const[first, second]=restaurant.categories;//based on the number of variables we are declaring here, it will assign that number of values from the array, from left to right.
console.log(first,second);//output will be Italian Pizzeria.

const[h,,j]=restaurant.categories;//here we have not declared 2 nd variable , so it will skip the second value in array.
console.log(h,j);//output will be Italian Vegetarian

//destructuring objects
const{name, openingHours,categories}=restaurant;//variable names should be same as property names otherwise the undefined value will be assigned.
console.log(name,openingHours,categories);

const{starterMenu:starters}=restaurant;//If we want to change the variable name should be defined in this way.
console.log(starters);

